					<h1>Infinitives</h1>
					<p><b><i>Infinitive</i></b> adalah bentuk dasar dari <i>verb</i>. Dalam bahasa  Inggris, penulisan <i>infinitive</i> biasanya diawali dengan <i>"to"</i>. Misalnya: <i>to read, to walk, to give,</i> dan seterusnya. Meskipun pada umumnya <i>infinitive</i> diawali dengan <i>"to"</i>, akan tetapi ada beberapa <i>infinitive</i> tanpa <i>"to"</i>, biasanya disebut dengan <b><i>bare infinitive</i></b>. Contoh : <i>Help me open the window</i>.
					<p><strong>Bare Infinitive</strong> adalah infinitive yang tidak diikuti oleh particle&nbsp;&ldquo;to&rdquo;. Kata ini dikenal juga dengan sebutan&nbsp;<em>base form</em>, <em>short infinitive</em>, atau <em>zero infinitive</em>. yang merupakan satu dari dari lima&nbsp;<em>verb form</em>&nbsp;yang dimiliki&nbsp;oleh&nbsp;<em>verb</em>.</p>
