<p><strong>Penulisan By Agent</strong></p>
<p>Dalam penulisan kalimat pasif, kita biasanya menggunakan "<em>by</em>" untuk menunjukkan <em>agen</em><em>t</em> atau pelaku - orang atau benda yang melakukan pekerjaan atau yang menyebabkan sesuatu terjadi. Penulisan "<em>by agent</em>" pada kalimat pasif ditujukan untuk menambah informasi dalam kalimat tersebut.</p>
<p><strong>Contoh :<br /></strong></p>
<p>This house was built <span style="text-decoration: underline;">by my grandfather</span>.<br />This great novel was written <span style="text-decoration: underline;">by JJ. Tolkien</span>.</p>
<p>Tetapi tidak semua kalimat pasif harus mencantumkan <em>"by agent</em>". Kita boleh tidak menyertakan "<em>by agent</em>" dalam pembuatan kalimat pasif jika orang atau benda yang melakukan pekerjaan tidak diketahui atau tidak penting.</p>
<p><strong>Contoh :<br /></strong></p>
<p>A lot of money was stollen in the robbery. (Tidak diketahui pelakunya)<br />The road is cleaned everyday. (Tidak terlalu penting pelakunya)</p>
