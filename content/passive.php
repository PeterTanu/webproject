<p><strong>Kalimat Pasif Pada Infinitive</strong></p>
<p>Bentuk pasif dari <em>infinitive phrase</em> dapat berperan sebagai subjek, objek, maupun <em>modifier</em> pada suatu kalimat. Rumus dan contoh bentuk pasif pada infinitif adalah sebagai berikut :</p>
<table class="cTabled" style="width: 200px; background-color:Gold;">
<tbody>
<tr>
<td>(to) be + past participle</td>
</tr>
</tbody>
</table>
<p><strong>Contoh :<br /></strong></p>
<ul>
<li>To be accompanied with him is a bad idea. (subjek)</li>
<li>Everyone needs to be loved. (objek)</li>
<li>He is the man to be trusted for all the things. (<em>modifier</em>)</li>
</ul>
</li>
