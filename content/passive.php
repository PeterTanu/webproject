<li><strong>Pengecualian pada Kata Kerja Transitif</strong>
<p>Tidak semua kata kerja transitif, kata kerja yang memiliki objek langsung dapat dipastikan. Beberapa kata kerja tersebut yang antara lain : have, become, lack, look like, mean, dll akan terdengar tidak wajar maknanya ketika dipasifkan.</p>
<p>Beberapa contoh kalimat dari kata kerja tersebut adalah sebagai berikut:</p>
<ul>
<li>I have a great new idea, ->; tidak dapat dipasifkan menjadi : A great new idea is had by me.</li>
<li>The snack contains aspartame. ->; tidak dapat dipasifkan menjadi : Aspartame is contained by the snack.</li>
</ul>
<p>Kalimat pasif penting untuk dipelajari saat kita ingin menempatkan fokus pada objek sebuah tindakan, dan bukannya pada orang yang melakukan tindakan tersebut.</p>
<p>Beberapa contohnya seperti yang ada di bawah ini:</p>
<ul>
<li>Somebody made my car in Detroit. ->; My car was made in Detroit.</li>
<li>Somebody painted this beautiful picture. ->; This picture was painted by George Smith.</li>
<li>A number of people in company XYZ have designed this recorder. ->; This recorder was designed by the ZYZ Company.</li>
</ul>
</li>
<li>
<p><strong>Grammar Kalimat Pasif </strong></p>
<p><em>Passive voice </em>digunakan saat fokus kalimat diletakkan pada orang atau benda yang terpengaruh oleh sebuah kejadian.</p>
<ul>
<li>Rumus pasif : <strong>Passive Subject + To Be + Past Participle</strong> . (<em>The house was built in 1989</em>).</li>
<li>Bentuk ini sering digunakan dalam bisnis saat objek sebuah tindakan lebih penting dibanding mereka yang melaksanakan tindakan tersebut.</li>
</ul>
<p><strong>Contoh :</strong></p>
<p><em>We have produced over 20 different models in the past two years.</em><br /><strong>Diubah menjadi</strong> : <em>Over 20 different models have been produced in the past two years</em>.</p>
<ul>
<li>Jika <em>agent</em>nya dirasa penting (orang, perusahaan, atau pelaku tindakannya), gunakan kata "<em>by</em>".</li>
</ul>
<p><strong>Contoh :</strong></p>
<p><em>Tim Wilson wrote "The Flight to Brunnswick" in 1987.</em><br /><strong>Diubah menjadi</strong> : <em>"The flight to Brunnswick" was written in 1987 by Tim Wilson.<br /></em></p>
<ul>
<li>Hanya kata kerja yang memiliki sebuah objek yang dapat digunakan dalam <em>passive voice</em>.</li>
</ul>
</li>
