					<h1>Verb Phrases</h1>
					<p><i>Verb Phrases</i> atau frasa verba adalah frasa yang tersusun dari verba utama ditambah dengan <i>auxiliaries, adverbs, adverb phrases (or clauses), prepositional phrases,</i> atau <i>object</i>.
					Dalam kalimat, frasa verba berfungsi sebagai predikat.
					<br><b>Contoh :</b>
					<ul>
						<li>He <b>was smoking.</b></li>
						<li>After she <b>had learned to drive,</b> Alice <b>felt more independent</b>.</li>
						<li>We <b>will meet at the library at 3:30 p.m.</b></li>
						<li>Henry <b>made my coach very proud.</b></li>
					</ul>
					</p>
					<p>Dalam frasa verba, <i>verb</i> selalu menjadi <i>head</i>, yang dapat disertai <i>pre-modifiers</i> dan/ atau <i>post-modifier</i>. Jika terdapat <i>pre-modifier</i>. Jika terdapat <i>pre-modifiers</i>, bisa berupa kata negatif (<i>not/never</i>) atau <i>adverb phrase</i>.
					<ul>
						<li><u>not</u> <b>say</b> what he is doing.</li>
						<li><u>never</u> <b>needs</b> money.</li>
						<li>He <u>deliberately</u> <b>broke</b> the window.</li>
					</ul>
					</p>
					<p>Kebanyakan <i>head</i> verba harus diikuti oleh <i>post-modifiers</i>.
					<ul>
						<li>My son <b>made</b> a cake.</li>
						<li>We <b>keep</b> pigeons.</li>
						<li>I <b>recommend</b> the fish.</li>
					</ul>
					</p>
					<p>Verba yang memerlukan <i>post-modifiers</i> biasa disebut <i>transitive verbs</i>. <i>Post-modifiers</i> pada contoh di atas disebut juga objek langsung (<i>direct object</i>) atau komplemen (<i>complement of the head</i>).
					<br>Sebaliknya, beberapa verba (<i>intransitive verbs</i>) digunakan tanpa objek langsung:
					<ul>
						<li>Susan <b>smiled</b>.</li>
						<li>The professor <b>yawned</b>.</li>
					</ul>
					</p>
					<p>Tetapi, banyak juga verba dalam bahasa Inggris yang berlaku sebagai <i>transitive</i> dan <i>intransitive</i>, tergantung cara pemakaiannya dalam kalimat. Berikut adalah contoh penggunaan kedua bentuk verba tersebut:
					<ul>
						<li>Mark <b>smokes</b>. (Intransitive)</li>
						<li>Mark <b>smokes cigars</b>. (Transitive)</li>
					</ul>
					</p>
					<p>Objek atau komplemen yang melengkapi frasa verba tidak hanya objek langsung (<i>direct object</i>) tetapi juga dapat berupa objek tidak langsung (<i>indirect object</i>).
					<ul>
						<li>We gave <b>James</b> a present.</li>						
					</ul>
					</p>
					<p>Perhatikan juga contoh berikut yang menggunakan verba <i>to be</i> sebagai <i>head</i>:
					<ul>
						<li>David <b>is</b> a musician.</li>		
						<li>Amy <b>is</b> clever.</li>
						<li>Our car <b>is</b> in the car park.</li>						
					</ul>
					</p>
