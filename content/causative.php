<h1>Causative Verb</h1>
<p><em><strong>Causative verb </strong></em>adalah kata kerja yang digunakan untuk menunjukkan bahwa subjek tidak bertanggung jawab langsung terhadap aksi yang terjadi melainkan seseorang atau sesuatu yang lain yang melakukan aksi tersebut.</p>
<p>&nbsp;</p>
<ol>
<li><strong><em>Fungsi </em><em>dan rumus Causative Verb</em></strong>
<p>Kalimat <em>causative verb</em> terbagi menjadi 2 macam, yaitu <em>active</em> dan <em>passive causative.</em> Pada kalimat <em>active causative verb</em>, <em>agent</em> (yang mengerjakan aksi) diketahui. Sebaliknya, pada kalimat <em>passive causative verb</em>, <em>agent</em> biasanya tidak disebutkan.</p>
<p><em><strong>Let, make, have</strong></em> dan <strong><em>get</em></strong> merupakan <em>causative verb </em>yang umum digunakan.</p>
<p>Fungsi dan rumus <em>let, make, have, </em>dan <em>get</em> adalah sebagai berikut:</p>
