					<h2>Past Future Continuous Tense</h2>
					<p><i>Past future continuous tense</i> adalah suatu bentuk kata kerja untuk menyatakan apa yang akan sedang dilakukan di masa depan ketika berada di masa lalu. Seperti <i>future continuous tense</i>, bentuk ini digunakan untuk membuat rencana atau prediksi serta cenderung menyebutkan waktu yang spesifik.
					</p>
