					<h2>Past Future Perfect Tense</h2>
					<p><i>Past future perfect tense</i> adalah suatu bentuk kata kerja untuk membicarakan aktivitas yang akan telah dilakukan di masa lalu. Bentuk ini biasa digunakan pada <i>main clause</i> dari <i>conditional sentence</i> tipe 3.
					</p>
						<ol>
							<li><p><b><i>Pola kalimat/rumus past future perfect tense</i></b>
							<br><i>Past future perfect tense</i> dibentuk dengan <i>auxiliary verb "would"</i> atau <i>"should"</i> dan <i>"have"</i>, serta <i>past participle</i>(V<sub>3</sub>). <i>"Have"</i> digunakan baik untuk <i>singular</i> maupun <i>plural subject</i>.
							</p>
							Sedangkan <i>past participle</i> dapat berupa <i>regular</i> atau <i>irregular verb</i>.
							<p>Dengan demikian rumus <i>past future perfect tense</i> untuk kalimat positif, negatif, dan interogatif adalah sebagai berikut:
