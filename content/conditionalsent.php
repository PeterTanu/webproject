<h1>Conditional Sentence</h1>
<p>Kalimat pengandaian (<em>conditional sentence</em>) adalah suatu bentuk kalimat majemuk yang dapat kita gunakan ketika kita ingin mengatakan bahwa sesuatu merupakan akibat atau konsekuensi yang bergantung pada situasi lain. Di dalamnya terdapat klausa pengandaian (<em>if clause</em>) dan klausa akibat (<em>result clause</em>). Pada bentuk <em>conditional sentences</em> kita menggunakan kata "<em>if</em>" (jika).</p>
<ol>
<li><strong><em>Bentuk - Bentuk dan Contoh Conditional Sentence</em></strong>
<ol style="list-style-type: lower-alpha;">
<li><strong>Benar pada waktu sekarang dan masa akan datang (<em>true</em> <em>in the present or future</em>)</strong>
<p>Bentuk <em>conditional sentence</em> ini digunakan untuk mengekspresikan pengandaian dengan harapan yang dikatakan oleh si pembicara mungkin terjadi untuk masa sekarang ataupun akan datang.</p>
