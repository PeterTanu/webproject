</li>
<li><strong>Tidak benar atau Berlawanan dengan Kenyataan pada Masa lalu (<em>Untrue in the Past</em>).</strong>
<p>Bentuk <em>conditional sentence</em> ini digunakan apabila kita ingin membayangkan suatu kemungkinan lain dari suatu perbuatan atau peristiwa yang telah terjadi di masa lampau. Atau dapat dikatakan bahwa bentuk ini dapat digunakan untuk menyatakan suatu keadaan atau peristiwa yang berlawanan dengan kenyataan yang sebenarnya di masa lampau. Sikap pembicara dalam hal ini adalah sangat menyesal terhadap perbuatan yang telah terjadi.</p>
