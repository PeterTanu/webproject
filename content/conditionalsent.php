<p>Kapan kita menggunakan <em>mixed conditional sentence</em>? <em>Mixed conditional sentence</em> digunakan untuk membicarakan tentang peristiwa atau situasi yang tidak terjadi di waktu lampau tetapi kita bisa membayangkan kemungkinan hasilnya di waktu sekarang atau sebaliknya.</p>
<p><strong>Contoh:</strong></p>
<ul>
<li>If you <em>lived</em> near the factory, you <em>would have heard</em> the sound of the explosion. Kenyataanya, kamu tidak tinggal dekat pabrik. Oleh karena itu kamu tidak mendengar suara ledakan itu.</li>
<li>If he <em>had not been</em> late this morning, he <em>would be permitted</em> to join the test. Kenyataannya, dia sudah terlambat. Oleh karena itu dia tidak diizinkan untuk mengikuti tes.</li>
