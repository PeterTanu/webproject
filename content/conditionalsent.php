</p>
<p><strong>Contoh lain :</strong></p>
<p><strong>If I'm not busy</strong>, I <strong>come </strong>to your party.</p>
<p><strong>If the weather is nice </strong>tomorrow, we <strong>will go </strong>on a picnic</p>
</li>
<li><strong>Tidak benar atau Berlawanan dengan Kenyataan pada Waktu Sekarang dan Masa Akan datang (<em>Untrue in the Present or Future</em>)</strong>
<p>Bentuk <em>conditional sentence</em> ini digunakan apabila kita ingin mengkhayalkan suatu keadaan yang bertentangan dengan kenyataan yang ada saat ini dan masa akan datang. Dalam hal ini sikap pembicara agak pesimis karena kecil kemungkinan peristiwa itu terjadi.</p>
