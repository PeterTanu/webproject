<p>Perhatikan contoh kalimat berikut ini:</p>
<p>If clause (simple past tense) - result clause (would+V<sub>1</sub>)</p>
<ol>
<li><strong>If I taught</strong> this class, I <strong>would not give </strong>tests. (Jika saya mengajar kelas ini, saya tidak akan memberikan tes.)
<p>Makna pada kalimat di atas: "kenyataannya si pembicara tidak mengajar kelas tersebut."</p>
</li>
<li><strong>If Sam were </strong>here right now, he <strong>would help</strong> us. (Jika Sam ada di sini sekarang, dia akan menolong kita.)
<p>Makna pada kalimat di atas: "kenyataannya, Sam tidak ada di sini sekarang."</p>
</li>
<li><strong>If I were</strong> you, I <strong>would accept</strong> this invitation. (Jika saya menjadi kamu, saya akan menerima undangan ini.)
<p>Makna pada kalimat di atas: "kenyataannya, saya bukan kamu."</p>
</li>
</ol>
