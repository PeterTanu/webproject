<p>Perhatikan contoh kalimat berikut ini:</p>
<ol>
<li><em><strong>If clause (simple present tense) - result clause (simple present tense)</strong></em>
<p><strong>If I don't eat</strong> breakfast, I always <strong>get</strong> hungry during class. (Jika saya tidak sarapan, saya selalu merasa lapar selama pelajaran di kelas.)</p>
<p>Pada contoh kalimat tersebut, <strong>result clause </strong>menggunakan <em>simple present tense</em>, fungsinya untuk mengekspresikan aktivitas yang sudah menjadi kebiasaan atau situasi. Makna pada kalimat tersebut. "Si pembicara merasa yakin bahwa dia terbiasa merasa lapar jika dia tidak sarapan sebelum berangkat ke sekolah."</p>
</li>
<li><em><strong>If clause (simple present tense) - result clause (simple future tense)</strong></em>
<p><em><strong>If I don't eat </strong>breakfast tomorrow, I <strong>will get</strong> hungry during class. </em>(Jika saya tidak sarapan besok, saya akan merasa lapar selama pelajaran di kelas.)</p>
<p>Pada contoh kalimat tersebut, <em><strong>result clause</strong></em> menggunakan <em>simple future tense,</em> fungsinya untuk mengekspresikan bahwa kalimat tersebut menyangkut aktivitas tertentu atau situasi di masa yang akan datang. Makna pada kalimat tersebut: "Si pembicara mengetahui bahwa dia akan merasa lapar jika besok dia tidak sarapan sebelum berangkat ke sekolah."</p>
</li>
</ol>
