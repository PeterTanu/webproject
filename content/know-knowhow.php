						<ol>
							<li><b><i>Know How + Infinitive</i></b>
							<p>Umumnya kita tidak menggunakan <b>know + to infinitive</b>. Kita menggunakan <b>know how + to infinitive</b>.
							<br><b>Contoh :</b>
							<br>Does he <b>know how to play</b> classical guitar music?
							<br>Bukan : <strike>Does he know to play</strike> ....
							</li>
							</p>
							<p>
							<li><i><b>Know + Object + Infinitive</i></b>
								<br>Dalam bahasa Inggris yang sangat formal, kita menggunakan <b>know + object + to-infinitive</b>.
								<br><b>Contoh : </b>
								<br>I have worked with John Davidson for five years and <b>know him to be</b> reliable and hard-working. Weather experts have never <b>known it to snow</b> as heavily as it has this spring.
							</li>
							</p>
							<p>
							<li><i><b>Know Tanpa Object</i></b>
							<br>Untuk merujuk pada pengetahuan umum, kita menggunakan <b>know</b> tanpa sebuah <b>object</b> :
							<br><b>Contoh :</b>
							<br>A: &nbsp;&nbsp; They're opening a new terminal at Amsterdam airport.
							<br>B: &nbsp;&nbsp; Yes, I <b>know</b>. (<i>I know that they are opening a new terminal.</i>)
							</p>
							<p>Kita menggunakan <b>know + object</b> untuk merujuk pada apa yang kita ketahui dari pengalaman pribadi :
							<br><b>Contoh :</b>
							<br>A: &nbsp;&nbsp; They're opening a new terminal at Amsterdam airport.
							<br>B: &nbsp;&nbsp; Yes, I <b>know</b> it. (<i>I know the one you mean. I have been there and seen the airport</i>.)
							</p>
							<p>Kadang kita juga menggunakan <i>know about</i> atau <i>know of</i> saat kita mengatakan apa yang kita ketahui dari pengetahuan umum (<i>know of</i> bersifat lebih formal);
							<br><b>Contoh : </b>
							<br>What do yo <b>know about</b> the origins of the American Civil War?
							<br>Bukan : <strike> What do you know the origins</strike> ....
							</li>
							</p>
