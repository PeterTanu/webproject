						<li><p><b><i>One-Word</i> dan <i>Phrasal Modal Verb</i></b>
						<br><i>Modal verb</i> merupakan kata yang muncul sebelum <i>main verb</i> untuk memodifikasi makna dari <i>main verb</i> tersebut. <i>Modal verb</i> dapat digolongkan menjadi <i>single</i> atau <i>one-word modal</i> (hanya terdiri dari satu kata) dan <i>phrasal modal verb</i> (umumnya dimulai dengan <i>to be</i> atau <i>have</i> dengan/tanpa diakhiri dengan <i>preposition to</i>).
						</p>
						<p>Contoh <i>one-word modal verb : will, would, may, might, should, can, could, must.</i></p>
						<p>Contoh <i>phrasal modal verb : be supposed to, be to, be able to, be going to, have to, ought to, would like to.</i></p>
						</li>
						<li><p><b>Kombinasi <i>One-Word</i> dengan <i>Phrasal Modal Verb</i></b>
						<br><i>One-word modal verb</i> dapat dikombinasikan dengan <i>phrasal modal verb</i> seperti yang ditunjukkan di bawah ini:
						<br><button class="btn btn-warning" disabled><b>modal verb + <u>phrasal modal verb</u></b></button>
						<br><b>Contoh :</b>
						<br>You will <u>have to</u> do over your task.
						<br>(Kamu akan harus mengerjakan kembali tugasmu.)
						</p>
						<p>Antara <i>phrasal modal</i> pun dapat dikombinasikan :
						<br><button class="btn btn-warning" disabled><b>phrasal modal + <u>phrasal modal</u></b></button>
						<br><b>Contoh : </b>
						<br>She is going to <u>have to</u> make a choice.
						<br>(Dia akan harus membuat sebuah keputusan.)
						</p>
						<p>Namun tidak ada
						<br><button class="btn btn-danger" disabled><strike><b>modal verb + <u>modal verb</u></b></strike></button>
						<br><b>Contoh : </b>
						<br>You <strike>can must</strike> work harder.
						</p>
						<p>Juga tidak ada
						<br><button class="btn btn-danger" disabled><strike><b>phrasal modal verb + <u>modal verb</u></b></strike></button>
