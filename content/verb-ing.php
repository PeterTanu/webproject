					<ol>
						<li><i>Verb dalam bentuk -ing</i>
						<p><br>Tata bahasa sangat penting dalam bahasa Inggris apalagi jika Anda senang menulis artikel atau bagi siapa saja yang ingin memperdalam kemampuan menulisnya dalam bahasa Inggris. Salah satu poin <i>grammar</i> yang harus dikuasai dengan benar adalah penggunaan bentuk kata kerja -ing.
						<br>Bentuk kata kerja -ing sangat penting untuk dikuasai khususnya jika pekerjaan anda berhubungan dengan tulis-menulis dalam bahasa Inggris. Menulis adalah komukasi tertulis. Jika komunkasi tertulis anda sangat terstruktur dengan tata bahasa yang baik dan terdiri atas kata-kata yang sesuai, maka karya tulis Anda akan banyak diminati oleh pembaca. Setidak-tidaknya mereka akan suka membacanya. Bentuk -ing adalah salah satu aspek penting dalam tata bahasa Inggris.
						</p></li>
						<li><i>Fungsi dan kegunaan bentuk -ing</i>
						<p><br>Bagaimana kita bisa menggunakan bentuk -ing dalam bahasa Inggris? Bentuk <i>-ing</i> digunakan sebagai <i>gerund</i> (kata benda yang dibentuk dari kata kerja berbentuk <i>-ing</i>). Kata kerja berbentuk <i>ing</i> digunakan setelah kata kerja tertentu, setelah kata depan (<i>in, on, upon, after, before, by, in spite of, instead of, without</i>), dan setelah beberapa kata sifat (<i>adjective</i>) tertentu.
						</p>
