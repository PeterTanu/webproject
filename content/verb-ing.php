						<p><i>Bentuk kata kerja -ing digunakan sebagai present participles</i>
							<ul>
							<li><i>Present participle</i> dapat digunakan sebagai penjelas kata benda (<i>noun modifier</i>). Posisinya berada di depan kata benda yang diterangkan.
								<dl>
								<dd>- It is a very <b>interesting</b> story. (<i>"interesting"</i> adalah <i>present participle</i> yang berfungsi sebagai <i>noun modifier</i>).</dd>
								<dd>- Look at that <b>burning</b> wood. (<i>"burning"</i> adalah <i>present participle</i>)</dd>
								</dl>
							</li>
							<li><i>Present participles</i> yang digunakan sebagai <i>noun modifier</i> juga bisa menunjukkan kegunaan benda yang diterangkan.
								<dl>
								<dd>- Use a <b>walking</b> stick to walk in the dark. (It is a stick for walking)</dd>
								<dd>- Go to the <b>waiting</b> room, please. (The room is for waiting)</dd>
								<dd>- I need a good <b>fishing</b> rod. (The rod is for fishing)</dd>
								</dl>
							</li>
							<li><i>Present Participle</i> digunakan pada <i>Present Continuous tense</i>.
								<dl>
								<dd>- Jane is <b>cooking</b> in the kitchen. (<i>cooking</i> = sedang memasak)</dd>
								<dd>- They are <b>working</b> in the garden. (<i>working</i> = sedang bekerja)</dd>
								<dd>- She is <b>reading</b> a book. (<i>reading</i> = sedang membaca)</dd>
								</dl>
							</li>
							<li><i>Present Continuous Tense</i> juga bisa dipakai untuk menyatakan rencana yang sudah diatur. Misalnya jika berpergian, Anda telah membeli tiketnya. Perhatikan contoh dalam kalimat berikut:
								<dl>
								<dd>- I am <b>flying</b> with Lion Air first thing in the morning tomorrow. (Ini berarti tiket pesawat sudah dibeli atau dipesan).</dd>
								<dd>- Lutfi and Fitri are <b>getting</b> married next week. (Undangan pesta sudah disebar).</dd>
								</dl>
							</li>
							<li><i>Present Participles</i> juga digunakan untuk menyatakan bahwa pekerjaan atau tindakan kedua terjadi pada saat bersamaan dengan tindakan pertama. Perhatikan dengan baik contoh-contoh berikut ini:
								<dl>
								<dd>- He read his book <b>drinking</b> a cup of coffee. (Dia membaca buku sambil minum kopi.)</dd>
								<dd>- My mom came home <b>carrying</b> a big bag on her back. (Ibu pulang sambil membawa tas besar di punggungnya.)</dd>
								<dd>- The teacher stood up in front of the classroom <b>explaining</b> the lesson. (Guru berdiri di depan kelas sambil menjelaskan pelajaran.)</dd>
								</dl>
							</li>
							<li><i>Present Participles</i> digunakan untuk menyatakan bahwa pekerjaan yang kedua terjadi sebagai akibat dari pekerjaan/tindakan pertama.
								<dl>
								<dd>- The plane crashed on the mountain <b>killing</b> all passengers on it. (Pesawat itu menabrak gunung, menewaskan semua penumpang.)</dd>
								<dd>- She fell over the bike <b>bruising</b> her knees and hands. (Ia jatuh dari sepeda sehingga mengakibatkan luka pada lutut dan tangannya.)</dd>
								<dd>- The bomb exploded <b>wounding</b> the people around. (Bom meledak sehingga melukai orang-orang di sekitar.)</dd>
								</dl>
							</li>
							</ul>
						</p></li>
					</ol>
					<br>Demikianlah antara lain penggunaan bentuk -ing yang berfungsi sebagai <i>present participles</i>.
