						<br>Karena <i>gerund</i> adalah kata benda, posisinya bisa sebagai subjek atau objek dalam kalimat.
							<ul>
							<li><b>Cooking</b> is my hobby. (<i>Cooking</i> adalah <i>gerund (noun)</i> yang berfungsi sebagai subjek)</li>
							<li>She hates <b>smoking</b>. (<i>Smoking</i> adalah <i>gerund</i> yang berfungsi sebagai objek yaitu objek kata kerja <i>hate</i>)</li>
							</p></ul>
						<p><br>Banyak terdapat kata kerja dan kata sifat yang diikuti oleh <i>gerund</i>. Berikut adalah beberapa kata kerja dan kata sifat tertentu yang diikuti oleh <i>gerund</i> (kata kerja bentuk -ing <i>tapi</i> bukan kata kerja).
						<br>
						<br><i>Gerund</i> setelah kata-kata kerja tertentu:
						<br><i>like, love, remember, forget, avoid, prevent, finish, start, begin, consider, postpone, practice, admit, allow, deny, stop, mind, keep, mention, miss, can't stand, go, delay, escape, need.</i>
						<br>
						<br><i>Gerund</i> setelah kata-kata sifat tertentu (kata sifat yang kebanyakan berpasangan dengan kata depan):
						<br><i>worth, keen on, interested in, worried about, afraid of, good at, disappointed about, crazy about, famous for, glad about.</i>
						<br>
						<br>Beberapa kata kerja dapat diikuti oleh <i>gerund</i> atau <i>"to infinitive"</i> namun artinya berbeda, misalnya:
						<br><i>"I stopped to talk"</i> dan <i>"I stopped talking".</i>
						<br><i>"I stopped to talk"</i> artinya saya berhenti untuk mengobrol, sedangkan pada <i>"I stopped talking"</i> berarti saya berhenti mengobrol.
						<br>Juga berhati-hatilah dengan kalimat ini karena artinya amat sangat berbeda:
						<br><i>"I remember going to Bali"</i> dan <i>"I remember to go to Bali".</i>
						<br>Dua kalimat tersebut sangat berbeda. Dalam kalimat yang pertama artinya Anda ingat waktu Anda pergi ke Bali, dan pada kalimat yang kedua Anda ingat untuk pergi ke Bali.
						<br>
						<br>Beberapa kata kerja jika diikuti oleh <i>gerund</i> atau <i>to infinitive</i> tetap mempunyai arti yang sama.
						<br><b>Contoh :</b>
							<ol>
								<li><i>Jane <b>likes to</b> talk to you</i> dan <i>Jane <b>likes talking</b> to you</i>. Kalimat <i>"Jane likes talking to you"</i> mempunyai kesan bahwa Jane pernah bercakap-cakap dengan Anda dan ia menyukai itu, sedangkan <i>"Jane likes to talk to you"</i> mempunyai kesan bahwa Jane baru menyampaikan keinginannya untuk mengobrol dengan Anda.
								<br>Contoh lain agar lebih jelas :
								<br>I <b>liked living</b> in London. (Saya pernah mengalami tinggal di London dan saya menyukainya.)<
								<br>I <b>like to live</b> in London. (Saya suka tinggal di London tapi mengkin belum pernah mengalami tinggal di sana.)
								</li>
								<li>Don't <b>neglect looking after</b> your house.
								<br>Don't <b>neglect to look after</b> your house.</li>
