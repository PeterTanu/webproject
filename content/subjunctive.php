	<th style="width: 361.883px; text-align: center;"><strong>Subject 1 + wish + Subject 2 + could/would + V<sub>1</sub></strong></th>
	</tr>
	</tbody>
	</table>
</div>
<p><strong>Contoh :</strong></p>
<p>I <strong>wish </strong>you would <strong>come</strong> to the party tonight.</p>
<p>(Artinya : Saya berharap kamu akan datang ke pesta nanti malam.)</p>
<p>(Faktanya : Dia tidak akan datang ke pesta nanti malam.)</p>
<p>&nbsp;</p>
<p><strong>CATATAN :</strong></p>
