<p><strong>Contoh :</strong></p>
<ul>
<li><p>She <strong>wished</strong> (that) she <strong>had</strong> more time last night.</p>
<p>(Artinya : Dia berharap bahwa dia punya waktu lebih kemarin malam.)</p>
<p>(Faktanya : Dia tidak punya waktu lebih kemarin malam.)</p></li>

<li><p>Jeff <strong>looked as though </strong>he <strong>had seen </strong>a ghost.</p>
<p>(Artinya : Jeff terlihat seolah-olah dia telah melihat hantu.)</p>
<p>(Faktanya : Jeff tidak melihat hantu.)</p></li>

<li><p>I <strong>would rather </strong>he <strong>had been </strong>here.</p>
<p>(Artinya : Saya lebih suka dia sudah berada di sini.)</p>
<p>(Faktanya : Dia tidak berada di sini.)</p></li>

<li><p><strong>If only </strong>Rachel <strong>had not been</strong> at home last night.</p>
<p>(Artinya : Seandainya Rachel tidak berada di rumah semalam.)</p>
<p>(Faktanya : Rachel berada di rumah semalam.)</p></li>
