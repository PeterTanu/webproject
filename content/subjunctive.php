<h1>Subjunctive</h1>
<p><em>Subjunctive </em>adalah kata kerja yang digunakan untuk membayangkan akibat dari sesuatu yang tidak ada atau tidak terjadi, yang dapat berupa harapan (<em>wish</em>), kebutuhan (<em>requirements</em>), dan saran (<em>suggestion</em>). Atau dengan kata lain, makna kalimat <em>subjunctive</em> selalu berlawanan dengan kenyataan atau fakta.</p>
<p>Kata - kata yang digunakan dalam <em>subjunctive adalah "</em><em><strong>wish</strong></em>" (menginginkan/mengharapkan), "<em><strong>as if/as though</strong></em>" (seolah-olah), "<em><strong>would rather</strong></em>" (lebih suka), "<em><strong>if only</strong></em>" (seandainya/jika saja).</p>
<ol>
<li><em><strong>Fungsi Subjunctive</strong></em>
<ol style="list-style-type: lower-alpha;">
<li>
<p><em>As if/as though</em> digunakan untuk menyatakan keadaan yang kenyataan atau faktanya tidak benar.</p>
</li>
<li>
<p><em>Wish</em><em>, would rather, </em>dan <em>if only</em> digunakan untuk menyatakan suatu harapan atau keinginan.</p>
</li>
</ol>
</li>
<li>
<p><em><strong>Rumus Pengunaan Subjunctive</strong></em></p>
</li>
<ol style="list-style-type: lower-alpha;">
<li>
<p><em><strong>Future Subjunctive</strong></em></p>
<p><em>Future Subjunctive</em> digunakan sebagai kalimat pengandaian yang mengacu pada masa yang akan datang, jadi faktanya juga dalam bentuk <em>simple future tense</em>.</p>
<div class="table-responsive">
	<table class="cTabled" style="width: 565px; height: 31px;">
