	<tr>
	<td style="width: 555px;">Pada bentuk <em>present subjunctive</em>, kata kerja yang digunakan adalah bentuk kedua (V<sub>2</sub>), dan jika harus menggunakan <em>to be</em>, harus menggunakan "<em>were</em>" untuk semua jenis subjek.</td>
	</tr>
	</tbody>
	</table>
</div>
<p><strong>Contoh :</strong></p>
<ul>
<li><p>I <strong>wish </strong>you <strong>visited</strong> me.</p>
<p>(Artinya : Saya berharap kamu mengunjungi saya.)</p>
<p>(Faktanya : Kamu tidak mengunjungi saya.)</p></li>

<li><p>The girl dresses <strong>as if</strong> it <strong>were </strong>summer now.</p>
<p>(Artinya : Gadis itu berpakaian seolah-olah sekarang musim panas.)</p>
<p>(Faktanya : Sekarang bukan musim panas.)</p></li>

<li><p>Shelly <strong>would rather</strong> he <strong>told</strong> the truth.</p>
<p>(Artinya : Shelly lebih suka dia mengatakan yang sebenarnya.)</p>
<p>(Faktanya : Dia tidak mengatakan yang sebenarnya.)</p></li>

<li><p><strong>If only </strong>she <strong>knew</strong>.</p>
<p>(Artinya : Seandainya dia tahu.)</p>
<p>(Faktanya : Dia tidak tahu.)</p></li>
</ul>
<p>&nbsp;</p>
</li>
<li>
<p><em><strong>Past Subjunctive</strong></em></p>
<p><em>Past Subjunctive</em> digunakan sebagai kalimat pengandaian yang mengacu pada masa lampau, jadi faktanya juga dalam bentuk <em>simple past tense</em>.</p>
