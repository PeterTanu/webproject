	<p>&nbsp;Pada contoh kalimat pertama di samping, fungsi <em><strong>be</strong></em> adalah <em><strong>subjunctive verb.</strong></em></p>
	<p>&nbsp;</p>
	<p><em>Subjunctive</em>&nbsp;digunakan pada&nbsp;<em>noun clause</em> yang diikuti oleh kata kerja (<em>verbs</em>), kata sifat (<em>adjectives</em>), dan ekspresi (<em>expressions</em>) tertentu.</p>
	<p>Pada contoh kalimat di samping,&nbsp;<em>subjunctive verb&nbsp;</em>digunakan hanya dalam bentuk sederhananya. Yakni, tidak mengandung unsur atau bentuk&nbsp;<em>present, past,&nbsp;</em>atau&nbsp;<em>future</em>. Tidak juga tunggal (<em>singular</em>) atau jamak (<em>plural</em>), jadi tidak ada penambahan&nbsp;<em>-s/-es</em> pada kata kerjanya.</p>
	<p>&nbsp;</p>
	<p><strong>Pada kalimat negatif : </strong></p>
	<p>not + <em>simple form </em>(bentuk sederhana / dari V<sub>1</sub>), seperti contoh ketiga.</p>
	<p>&nbsp;</p>
	<p><strong>Pada kalimat pasif :</strong></p>
	<p>Bentuk sederhana dari <em>be</em> + V<sub>3</sub>, seperti contoh keempat</p>
	</td>
	</tr>
	<tr style="height: 38px;">
	<td style="width: 196.65px; height: 38px; text-align: left;" colspan="2" >
	<p><em>Subjunctive</em> lebih sering digunakan pada <em>American English</em> daripada <em>British English</em>. Pada <em>British English</em>, (<strong><em>should, could, would</em></strong>) <strong>+ V<sub>1 </sub></strong>lebih biasa digunakan daripada <em>subjunctive.</em> <br />Perhatikan Contoh berikut:</p>
	<p><em>I <strong>suggested</strong> that <strong>she see </strong>a doctor.</em></p>
	<p><em>I <strong>suggested </strong>that <strong>she should see </strong>a doctor.</em></p>
	<p>Penggunaan kata "<em>should</em>" digunakan setelah kata <em>suggest</em> dan <em>recommend</em>.</p>
	</td>
	</tr>
	<tr style="height: 38px;">
	<td style="width: 196.65px; height: 38px; text-align: left;" colspan="2">
	<p><strong>Kata kerja yang lazim diikuti oleh <em>subjunctive</em> pada <em>noun clause</em>:</strong></p>
	<p><em>demand (that), suggest (that), insist (that), recommend (that), request (that), advise (that), ask (that), propose (that).</em></p>
	<p><strong>Kata sifat dan ekspresi yang lazim diikuti oleh <em>subjunctive</em> pada <em>noun clause</em>:<br /></strong></p>
	<p><em>It is important (that), It is necessary (that), It is essential (that), It is vital (that), It is imperative (that).</em></p>
	</td>
