	</tr>
	<tr style="height: 46px;">
	<td style="width: 151.5px; height: 46px;"><strong>Would rather</strong></td>
	<td style="width: 395.5px; height: 46px;"><strong>Subject 1 + would rather + Subject 2 + V<sub>2</sub>/were</strong></td>
	</tr>
	<tr style="height: 46px;">
	<td style="width: 151.5px; height: 46px;"><strong>If only</strong></td>
	<td style="width: 395.5px; height: 46px;"><strong>If only + Subject + V<sub>2</sub></strong></td>
	</tr>
	</tbody>
	</table>
</div>
<p>&nbsp;</p>
<p><strong>CATATAN :<br /></strong></p>
