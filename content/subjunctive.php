</div>
	<p>&nbsp;</p>
	</li>
	<li>
	<p><em><strong>Present Subjunctive</strong></em></p>
	<p><em>Present Subjunctive</em> digunakan sebagai kalimat pengandaian, yang mengacu pada masa sekarang, jadi faktanya juga dalam bentuk <em>simple present tense</em>.</p>
