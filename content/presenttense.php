						<b><i>Simple Present Tense</i></b> adalah <i>tense</i> umum yang sering kita gunakan. <i>Simple present tense</i> berfungsi untuk menyatakan kalimat yang bersifat fakta, kebiasaan, atau sesuatu yang erjadi setiap saat.
						<ol>
							<li>
								<b><i>Pola kalimat/rumus simple present tense</i></b><br>
									<button class="btn btn-warning" disabled><b>S + V<sub>1</sub> (-s atau -es)</b></button><br>
								Subjek merupakan sesuatu yang dijelaskan di dalam kalimat, bisa berupa orang ataupun suatu benda.
								<br><i>Verb</i> merupakan kata kerja yang menerangkan kegiatan apa yang dilakukan oleh si subjek.
								<br><i>Suffix</i>/akhiran <i>-s</i> atau <i>-es</i> kita tambahkan apabila subjek pada kalimat tersebut adalah orang ketiga (<i>he, she, it</i>).
								<br>Aturan penambahan <i>-s</i> atau <i>-es</i> ditentukan oleh huruf terakhir dari kata kerja tersebut. <i>Suffix -es</i> digunakan untuk <i>verb</i> yang berakhir dengan bunyi desis dan <i>"y"</i> dengan mengubah <i>"y"</i> menjadi <i>"i"</i> terlebih dulu.
								<br><b>Contoh :</b>
								<ul><li>kiss = kisses</li>
								<li>watch = watches</li>
								<li>try = tries</li>
								<li>say = says</li>
								<li>throw = throws</li>
								</ul>
								<br>Untuk <i>simple present tenses</i> yang tidak memiliki kata kerja kita dapat menambahkan <i>to be</i> sebelum kata bukan kata kerja.
							</li><br>
							<li>
							<b><i>Contoh simple present tense</i></b><br>
								<ul>
									<li><b>Ivan write</b> an artice every day.<br>(Ivan menulis sebuah artikal setiap hari)</li>
									<li><b>He gets up</b> at 5 o'clock every Sunday.<br>(Dia bangun tidur pukul 5 pagi setiap hari Minggu)</li>
									<li><b>They go to</b> English course every Tuesday and Saturday.<br>(Mereka pergi les bahasa Inggris setiap hari Selasa dan Sabtu)</li>
									<li><b>Toni is</b> a manager.<br>(Toni adalah seorang manager)</li>
									<li><b>Toba Lake is</b> the largest lake in Indonesia.<br>(Danau Toba adalah danau terbesar di Indonesia)</li>
 								</ul>
							</li><br>
							<li>
								<b><i>Bagaimanakah bentuk kalimat negatif dari simple present tense?</b></i>
								<br>Pola kalimat (<i>pattern</i>) sebagai berikut:<br>
								<button class="btn btn-warning" disabled><b>S + do/does + not + V<sub>1</sub> </b></button><br>
								Untuk kata yang bukan kata kerja kita tetap menggunakan <i>to be</i> dalam pembuatan kalimat negatifnya, dengan rumus:<br>
								<button class="btn btn-warning" disabled><b>S + to be + not + V<sub>1</sub></b></button><br>
								<b>Contoh :</b>
									<ul>
										<li><b>Ivan doesn't listen</b> a song everyday.<br>(Ivan tidak mendengarkan lagu setiap hari)</li>
										<li><b>Coffee doesn't come</b> from Indonesia.<br>(Kopi tidak berasar dari Indonesia)</li>
										<li><b>They don't go</b> to school today.<br>(Mereka tidak pergi sekolah hari ini)</li>
										<li><b>He isn't</b> a singer.<br>(Dia bukan seorang penyanyi)</li>
										<li><b>Indonesia isn't</b> a stupid country.<br>(Indonesia bukan negara yang bodoh)</li>
									</ul>
							</li><br>
							<li>
								<b><i>Bagaimanakah bentuk kalimat interogatif dari simple present tense?</i></b><br>
								Polanya adalah sebagai berikut:<br>
								<button class="btn btn-warning" disabled><b>Do/Does + S + V<sub>1</sub>? </b></button><br>
