								</ul>
								<p>Selain diawali kata <i>do</i>, kita bisa juga membuat kalimat tanya dengan menambahkan kata tanya, seperti <i>what, who, why, when, where, how, whom,</i> dsb untuk mendapatkan alasan dari jawaban si penjawab nantinya.<br>
