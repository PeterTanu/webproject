							<li><b>Penggunaan <i>Preferences</i></b>
							<p>Kita sering menggunakan kata seperti: <i>"prefer, would prefer, would rather"</i> untuk menanyakan tentang pilihan (<i>preferences</i>).</p>
							<p><b>Contoh : </b>
							<br>I prefer living with my uncle
							<br>Would you prefer to watch a movie or go to church?
							<br>Would you rather go fishing with me?
							</p>
							<p>Contoh kalimat di atas memiliki arti yang sedikit berbeda, ini merupakan alasan mengapa pelajar bahasa Inggris sering mengalami kesulitan.
							<br>Perhatikan baik-baik perbedaan penggunaan <i>preferences</i> di bawah ini:
								<ol>
									<li><b><i>Perbedaan pada arti :</i></b>
									<p>Kita cenderung menggunakan kata <i>"prefer"</i> untuk membicarakan tentang kesukaan, ketidaksukaan, dan apa yang kita inginkan.
									<br><b>Contoh :</b>
									<br>He <b>prefers</b> reading a magazine.
									<br>(Dia lebih suka membaca majalah.)
									</p>
									<p>I <b>prefer</b> going to the beach than going to a swimming pool.
									<br>(Saya lebih suka pergi ke pantai daripada pergi ke kolam renang.)
									</p>
									<p>Sedangkan untuk penggunaan kata <b><i>"would prefer"</b></i> dan <b><i>"would rather"</i></b>, digunakan untuk membicarakan sesuatu yang lebih spesifik.</p>
									<p><b>Contoh : </b>
									<br>I <b>would prefer</b> to see him personally.
									<br>(Saya lebih suka menemuinya secara pribadi.)
									</p>
									<p>I <b>would rather</b> go home now.
									<br>(Saya lebih memilih untuk pulang sekarang.)</p>
									</li>
									
									<li><b><i>Perbedaan pada bentuk kata kerjanya :</i></b>
									<p>I <b>prefer</b> living in a town.
									<br>(Diikuti <i><b>gerund</b></i>, menggunakan akhiran <i><b>"-ing"</b></i>)
									</p>
									<p>I <b>would prefer</b> to be told the truth.
									<br>(Diikuti oleh <b><i>infinitive</b></i>, menggunakan <b><i>"to + V<sub>1</sub>"</i></b>)
									</p>
									<p>Would you <b>rather</b> stay at an apartment?
									<br>(Diikuti oleh bentuk dasar dari kata kerja <b><i>V<sub>1</sub></i></b> tanpa <b><i>"to"</i></b>)
									</p>
									</li>
									
									<li><b><i>Perbedaan penggunaan preposisi (preposition) untuk menentukan pilihan :</b></i>
									<p><b><i>"Prefer"</i></b> dan <b><i>"Would prefer"</i></b> diikuti preposisi <i><b>"to"</b></i>.
									</p>
									<p><b>Contoh : </b>
									<br>I <b>prefer</b> living in a town <b>to</b> living in the village.
									<br>I <b>would rather</b> being alone <b>to</b> being with the wrong person.
									</p>
									<p><b><i>"Would rather"</b></i> diikuti preposisi <b><i>"than"</i></b>.</p>
									<p><b>Contoh : </b>
									<br>I <b>would rather</b> talk to him in person <b>than</b> call him on the phone.
